				<h3>1. ¿Qué hago si no encuentro mi escuela?</h3>
				<p>Nuestro sitio tiene perfiles de todas las escuelas primarias, secundarias y bachilleratos públicas y privadas registradas ante la SEP a nivel nacional <span class='red'>durante el último ciclo escolar</span>. Si intentaste buscar tu escuela por nombre y no te aparece en los resultados, intenta hacer una nueva búsqueda por zona geográfica. Si aún no encuentras tu escuela, escríbenos a contacto@mejoratuescuela.org y te ayudaremos a encontrarla.
				</p>
				<a name="pregunta2"></a>
				<h3>2. ¿Qué tan actualizada está la plataforma?</h3>
				<p>MejoraTuEscuela.org se alimenta de las bases de datos más actualizadas que están disponibles.  Para medir desempeño académico se utilizan los resultados de ENLACE del último ciclo escolar (2013).  La información sobre grupos, maestros, alumnos e infraestructura se basa en lo reportado en el Censo de Escuelas, Maestros y Alumnos de Educación Básica y Especial, realizado por el INEGI y la SEP en el ciclo escolar 2013/2014. 
				<br />
				La información sobre programas de apoyo del Gobierno Federal y de organizaciones de la sociedad civil (OSCs) se origina del censo educativo así como de lo reportado en padrones oficiales de cada programa y por las organizaciones mismas. 
				</p>
				<a name="pregunta3"></a>
				<h3>3. ¿Qué es la prueba ENLACE?</h3>
				<p>La Evaluación Nacional de Logro Académico en Centros Escolares (ENLACE) es una prueba del Sistema Educativo Nacional, cuyo objetivo es contribuir al avance educativo de los alumnos mexicanos de cada centro escolar y entidad federativa.
				<br />				
				Hasta el ciclo escolar 2012/2013, la prueba se aplicaba a los alumnos de primaria de tercer a sexto grado.
				<br />
				También hasta el 2013, la prueba se aplicó a jóvenes de primero, segundo y tercero de secundaria.
				En nivel medio superior, la prueba sigue vigente y se administra a los estudiantes que cursan el último año.
				<br />
				Las pruebas cubren temas en función de los planes y programas de estudios oficiales en los campos de comunicación y comprensión lectora en español y en el campo de las matemáticas. La prueba incluye también una tercer asignatura que cambia cada año, por ejemplo, en el 2008 fue ciencias, en el 2009 formación cívica, en el 2010 historia, en el 2011 geografía y en el 2012 nuevamente ciencias.
				<br />
				El propósito de ENLACE es generar una sola escala de carácter nacional que proporcione información comparable de los conocimientos y habilidades que tienen los estudiantes en los temas evaluados. Para más información sobre la prueba, visita: <a href="http://enlace.sep.gob.mx/">http://enlace.sep.gob.mx/</a>
				</p>
