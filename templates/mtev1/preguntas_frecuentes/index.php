				<a name="pregunta6"></a>
				<h3>6. ¿Por qué mi escuela tiene una mala calificación en el semáforo educativo si mi hij@ está obteniendo buenas calificaciones en la escuela?
				</h3>
				<p>Las calificaciones individuales de tu hij@ son independientes del desempeño de la escuela en conjunto o de su calidad educativa. En la boleta de calificaciones de la escuela, tu hij@ puede tener resultados muy altos, pero eso no quiere decir que está cumpliendo con los estándares nacionales de aprendizaje o que la escuela, en su conjunto, los esté cumpliendo. Te sugerimos revisar los resultados de tu hij@ en la prueba ENLACE, su calificación te puede dar un mejor estimado de su nivel de aprendizaje comparad@ con otros estudiantes de su edad. Puedes conocer los resultados en:<br />
				Para primaria y secundaria: <a href="http://bit.ly/1dAOfs6">http://bit.ly/1dAOfs6</a><br />
				Y para bachillerato en: <a href="http://bit.ly/15t9pWS">http://bit.ly/15t9pWS</a> 
				</p>
				<a name="pregunta7"></a>
				<h3>7. ¿Qué quiere decir el triángulo negro junto al nombre de mi escuela (o en el lugar del semáforo educativo)?</h3>
				<p>El triángulo negro junto al nombre de una escuela indica que la escuela no administra la prueba ENLACE a sus alumnos. Por lo tanto, no tenemos manera de obtener información para definir su calidad educativa. Si este es el caso para tu escuela, te invitamos a que firmes una petición para que la prueba ENLACE se administre a todas las escuelas de tu estado. Encuentra las distintas peticiones aquí.
				</p>
				<a name="pregunta8"></a>
				<h3>8. ¿Qué quiere decir el triángulo gris junto al nombre de mi escuela (o en el lugar del semáforo educativo)?
				</h3>
				<p>El triángulo gris indica que no todos los grados de la escuela toman la prueba ENLACE. Para los años que no tenemos resultado se asigna un cero, por lo tanto, el promedio de la escuela tiende a ser muy bajo. Esta situación se puede dar en dos ocasiones:<br />
				La escuela toma la decisión de que uno de sus grados no tome la prueba ENLACE.<br />
				La escuela es nueva y aún no tiene estudiantes en todos los grados.<br />
				</p>
				<a name="pregunta9"></a>
				<h3>9. ¿Qué quiere decir el triángulo rojo junto al nombre de mi escuela (o en el lugar del semáforo educativo)?</h3>
				<p>La prueba ENLACE utiliza tecnologías avanzadas para garantizar el control de calidad de los resultados individuales y por grupo. La SEP identifica como “copia u otras acciones inaceptables” cualquier tendencia que indique la existencia de copia, dictado de respuestas y otras prácticas fraudulentas. El símbolo de precaución junto al nombre de una escuela indica que la SEP considera más del 10% de las pruebas ENLACE administradas por esa escuela como “no confiables” o “fraudulentas”.
<br />
				Si tu escuela aparece con triángulo rojo, invitamos a que te acerques con el director o directora y los maestros para que te expliquen qué están haciendo para solucionar estas problemáticas. 
				</p>
